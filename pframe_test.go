// TestDecodePFrameAllSkip verifies the skip MB path using a hand-crafted all-skip
// P-frame (header + 64 skip-bits all=1). With a grey reference the output must
// equal the reference (no pixels changed).
func TestDecodePFrameAllSkip(t *testing.T) {
	// All-skip P-frame: pictype=01 quant=8 use_mb_skip=1 rcIdx=2 dcIdx=1 mvIdx=1,
	// then 64 skip bits all=1, padded to 10 bytes.
	allSkip := []byte{0x51, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xF0}
	W, H := 128, 128
	// nil reference fills with grey (128)
	pImg, err := DecodePFrame(allSkip, nil, W, H)
	if err != nil {
		t.Fatalf("DecodePFrame all-skip: %v", err)
	}
	// Every luma pixel must be 128
	for i, v := range pImg.Y {
		if v != 128 {
			t.Errorf("Y[%d]=%d want 128", i, v)
			break
		}
	}
	t.Log("all-skip P-frame: PASS")
}

func TestDecodePFrameSkip(t *testing.T) {
	W, H := 128, 128

	// Decode reference I-frame
	ref, err := DecodeIntraFrame(pTestFrameI, W, H)
	if err != nil {
		t.Fatalf("DecodeIntraFrame: %v", err)
	}

	// Decode P-frame (should be all-skip → identical to ref)
	pImg, err := DecodePFrame(pTestFrameP1, ref, W, H)
	if err != nil {
		t.Fatalf("DecodePFrame: %v", err)
	}

	// Compare: P-frame output must closely match reference
	var sumDiff float64
	maxDiff := 0
	for i, rv := range ref.Y {
		pv := pImg.Y[i]
		d := int(rv) - int(pv)
		if d < 0 { d = -d }
		if d > maxDiff { maxDiff = d }
		sumDiff += float64(d)
	}
	mse := sumDiff / float64(len(ref.Y))
	t.Logf("P-frame vs ref: MSE=%.2f maxDiff=%d", mse, maxDiff)
	fmt.Printf("DecodePFrame skip test: MSE=%.2f maxDiff=%d\n", mse, maxDiff)
	if mse > 1.0 {
		t.Errorf("MSE too high: %.2f (want ≤ 1.0 for skip P-frame)", mse)
	}
}
